
//The class is in Upper Camel Case  (The file and the class must be the same name )
public class NamingInJava {
    public static void main(String[] args) {
        int $cellphone = 1684616811;
        int $Cellphone = 346546413;

        System.out.println($cellphone);
        System.out.println($Cellphone);

        String _variable = "Other data";

        System.out.println(_variable);

        //It is conventional to write constants in uppercase.

        int CONSTANT_NUMERIC = 100;
        String CONSTANT_ALPHA = "This is a constant";

        System.out.println(CONSTANT_NUMERIC +" "+ CONSTANT_ALPHA);

        //For the variables is common to write in lower camel case

        int sizeInCentimeters = 26;

        System.out.println(sizeInCentimeters);
    }
}
